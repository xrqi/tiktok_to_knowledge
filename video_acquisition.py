    async def resolve_short_url(self, short_url: str) -> Optional[str]:
        """将短链接转换为长链接"""
        try:
            if not short_url.startswith(('http://', 'https://')):
                short_url = 'https://' + short_url
            
            headers = {
                'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36',
                'Accept': 'text/html,application/xhtml+xml,application/xml;q=0.9,image/webp,*/*;q=0.8',
                'Accept-Language': 'zh-CN,zh;q=0.9,en;q=0.8',
                'Accept-Encoding': 'gzip, deflate, br',
                'Connection': 'keep-alive',
            }
            
            async with aiohttp.ClientSession() as session:
                async with session.get(short_url, headers=headers, allow_redirects=True, timeout=aiohttp.ClientTimeout(total=10)) as response:
                    if response.status == 200:
                        long_url = str(response.url)
                        self.logger.info(f"短链接转换成功: {short_url} -> {long_url}")
                        return long_url
                    else:
                        self.logger.warning(f"短链接转换失败，状态码: {response.status}")
                        return None
        except Exception as e:
            self.logger.error(f"短链接转换出错: {e}")
            return None

    def extract_platform_and_id(self, url: str) -> Optional[Tuple[str, str]]:
        """从URL提取平台和视频ID"""
        # 抖音URL模式
        douyin_patterns = [
            r'douyin\.com/(\w+)',
            r'douyin\.com.*video/(\w+)',
            r'v\.douyin\.com/(\w+)',
            r'v\.douyin\.com/(\w+)/'
        ]
        
        # 快手URL模式
        kuaishou_patterns = [
            r'kuaishou\.com/(\w+)',
            r'kuaishou\.com/.*photoId=(\w+)',
            r'gifshow\.com/(\w+)',
            r'gifshow\.com/.*photoId=(\w+)'
        ]
        
        for pattern in douyin_patterns:
            match = re.search(pattern, url)
            if match:
                return 'douyin', match.group(1)
        
        for pattern in kuaishou_patterns:
            match = re.search(pattern, url)
            if match:
                return 'kuaishou', match.group(1)
        
        return None
    
