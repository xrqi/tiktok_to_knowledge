    def _convert_json_cookies_to_netscape(self, json_cookies_path: str) -> str:
        """
        Convert JSON cookies to Netscape cookie format that yt-dlp can use
        Returns path to temporary cookie file
        """
        if not os.path.exists(json_cookies_path):
            return None
        
        with open(json_cookies_path, 'r', encoding='utf-8') as f:
            cookies = json.load(f)
        
        # Create a temporary file for the cookies in Netscape format
        cookie_file = tempfile.NamedTemporaryFile(mode='w', delete=False, suffix='.txt')
        
        # Write Netscape cookie format header
        cookie_file.write("# Netscape HTTP Cookie File\n")
        cookie_file.write("# This file was generated by video_acquisition.py\n")
        cookie_file.write("# Do not edit this file manually\n\n")
        
        # Check if required cookies exist, especially s_v_web_id for Douyin
        has_s_v_web_id = any(cookie.get('name') == 's_v_web_id' for cookie in cookies)
        has_ttwid = any(cookie.get('name') == 'ttwid' for cookie in cookies)
        
        # If we don't have the required cookies for Douyin, we'll still process what we have
        # but log a warning
        if not has_s_v_web_id:
            self.logger.warning("Missing required 's_v_web_id' cookie for Douyin")
            # Generate a fake s_v_web_id cookie if missing - this might help bypass the check
            self._add_fallback_s_v_web_id_cookie(cookie_file)
        if not has_ttwid:
            self.logger.warning("Missing 'ttwid' cookie which may be required for Douyin")
        
        for cookie in cookies:
            domain = cookie.get('domain', '')
            # The flag should be TRUE if the domain starts with a dot (allow subdomains)
            flag = 'TRUE' if domain.startswith('.') else 'FALSE'
            path = cookie.get('path', '/')
            secure = 'TRUE' if cookie.get('secure', False) else 'FALSE'
            
            # Handle expires value - if it's -1 (session cookie), set to a future date
            expires_raw = cookie.get('expires', 0)
            if expires_raw == -1 or expires_raw is None:
                # For session cookies or missing expires, set to a reasonable future time (e.g., 1 year from now)
                expires = str(int(time.time()) + 365 * 24 * 60 * 60)  # 1 year in seconds
            elif isinstance(expires_raw, (int, float)) and expires_raw > 0:
                # If the cookie is not expired, use its expiration time
                current_time = time.time()
                if expires_raw > current_time:
                    expires = str(int(expires_raw))
                else:
                    # Cookie is expired, set to a future time
                    expires = str(int(current_time + 365 * 24 * 60 * 60))
            else:
                # For other cases, set to a future time
                expires = str(int(time.time()) + 365 * 24 * 60 * 60)
            
            name = cookie.get('name', '')
            value = cookie.get('value', '')
            
            # Skip cookies without essential fields
            if not name or not value:
                continue
                
            # Write the cookie in Netscape format
            cookie_line = f"{domain}\t{flag}\t{path}\t{secure}\t{expires}\t{name}\t{value}\n"
            cookie_file.write(cookie_line)
        
        cookie_file.close()
        return cookie_file.name
    
    def _add_fallback_s_v_web_id_cookie(self, cookie_file):
        """
        Add a fallback s_v_web_id cookie that might help bypass the requirement check
        """
        import random
        import string
        
        # Generate a fake but plausible s_v_web_id value
        # This is typically a verification ID for web sessions
        chars = string.ascii_letters + string.digits
        s_v_web_id_value = 'verify_' + ''.join(random.choice(chars) for _ in range(19)) + '_' + ''.join(random.choice(chars) for _ in range(10))
        
        # Write the cookie in Netscape format
        cookie_line = f".douyin.com\tTRUE\t/\tFALSE\t{int(time.time()) + 365 * 24 * 60 * 60}\ts_v_web_id\t{s_v_web_id_value}\n"
        cookie_file.write(cookie_line)
    
    def check_cookies_validity(self, cookies_path: str = 'douyin_cookies.json') -> bool:
        """
        检查cookies文件是否存在且包含必要的cookie
        对于抖音，需要检查是否包含登录相关的关键cookie
        """
        if not os.path.exists(cookies_path):
            self.logger.warning(f"Cookies文件不存在: {cookies_path}")
            return False
        
        try:
            with open(cookies_path, 'r', encoding='utf-8') as f:
                cookies = json.load(f)
            
            if not cookies:
                self.logger.warning("Cookies文件为空")
                return False
            
            # 检查是否有过期的cookies
            current_time = time.time()
            valid_cookies = []
            
            for cookie in cookies:
                expires = cookie.get('expires', 0)
                # 如果expires为-1表示会话cookie，通常认为是有效的
                if expires == -1:
                    valid_cookies.append(cookie)
                elif isinstance(expires, (int, float)) and expires > current_time:
                    valid_cookies.append(cookie)
                elif expires == 0 or expires is None:
                    # 没有设置过期时间的cookie，认为是有效的
                    valid_cookies.append(cookie)
            
            # 检查是否包含抖音登录所需的关键cookies
            # 抖音需要登录状态的cookies，包括 sessionid, sessionid_ss, passport_csrf_token 等
            required_cookies = ['sessionid', 'sessionid_ss', 'passport_csrf_token', 'sid_guard']
            has_login_cookies = any(cookie.get('name') in required_cookies for cookie in valid_cookies)
            
            # 检查其他重要的cookies
            has_s_v_web_id = any(cookie.get('name') == 's_v_web_id' for cookie in valid_cookies)
            has_ttwid = any(cookie.get('name') == 'ttwid' for cookie in valid_cookies)
            
            if not has_login_cookies:
                self.logger.warning("缺少抖音登录所需的关键cookies (sessionid, sessionid_ss, passport_csrf_token, sid_guard)")
                # 如果没有登录cookies，即使有其他cookies也认为无效
                return False
            
            if not has_s_v_web_id:
                self.logger.warning("缺少关键的s_v_web_id cookie")
            
            if not has_ttwid:
                self.logger.warning("缺少ttwid cookie")
            
            # 如果有至少一些有效的cookies，我们认为cookies文件是有效的
            return len(valid_cookies) > 0
            
        except Exception as e:
            self.logger.error(f"检查cookies有效性时出错: {e}")
            return False

